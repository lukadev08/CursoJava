public class For3 {
    public static void main(String[] args) {
       
        for (int i = 0; i < 10; i++){
            // index do loop // incremento +1 ate 10
            for (int j = 0; j < 10; j++){
                System.out.printf("[%d %d]", i, j);
            }
            System.out.println();
        }
    }
}
