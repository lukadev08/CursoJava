public class WhileDeterminado {
    public static void main(String[] args) {
        int contador = 1;

        while (contador <= 20) {
            // --- while cria determinada repeticao da sentenca do codigo
            
            System.out.printf("i = %d\n", contador);
            contador += 2;
            // --- incrementa ++ 1 em 1, += num escolhido,  o "contador" ate tantas as vezes <= numero determinado (ex: <=10)
                 
        }
    }
}
